# Solicitar el primer numero
numero_1 = input('Digite su primer numero ')
numero_1 = float(numero_1)


# Solicitar el segundo numero
numero_2 = input('Digite su segundo numero ')
numero_2 = float(numero_2)

# Calcular la suma
resultado = numero_1 + numero_2
print('La suma es', resultado)

# Calcular la resta
resultado = numero_1 - numero_2
print('La suma es', resultado)

# Calcular el producto
resultado = numero_1 * numero_2
print('La suma es', resultado)

# Calcular la division entera
resultado = numero_1 // numero_2
print('La suma es', resultado)

# Calcular la division flotante
resultado = numero_1 / numero_2
print('La suma es', resultado)

# Calcular el modulo
resultado = numero_1 % numero_2
print('La suma es', resultado)